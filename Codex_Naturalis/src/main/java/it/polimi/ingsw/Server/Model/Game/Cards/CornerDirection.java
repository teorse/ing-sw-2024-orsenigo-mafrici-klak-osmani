package it.polimi.ingsw.Server.Model.Game.Cards;

/**
 * Enum representing the cardinal directions of corners on game cards.
 * <p>
 * Each direction corresponds to a specific corner on a card:
 * - NW: North-West corner
 * - NE: North-East corner
 * - SE: South-East corner
 * - SW: South-West corner
 */
public enum CornerDirection {
    NW,
    NE,
    SE,
    SW
}